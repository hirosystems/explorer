const ONE_SECOND = 1000;

/**
 * Represents one minute in milliseconds.
 * @constant {number}
 */
export const ONE_MINUTE = 60 * ONE_SECOND;

/**
 * Represents two minutes in milliseconds.
 * @constant {number}
 */
export const TWO_MINUTES = 2 * ONE_MINUTE;

/**
 * Represents five minutes in milliseconds.
 * @constant {number}
 */
export const FIVE_MINUTES = 5 * ONE_MINUTE;

/**
 * Represents ten minutes in milliseconds.
 * @constant {number}
 */
export const TEN_MINUTES = 10 * ONE_MINUTE;
